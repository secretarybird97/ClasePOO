public class Organism {

	private String name;
	private String reign;

	public String getName() {
		return this.name;
	}

	/**
	 * 
	 * @param name
	 */
	public void setName(String name) {
		this.name = name;
	}

	public String getReign() {
		return this.reign;
	}

	/**
	 * 
	 * @param reign
	 */
	public void setReign(String reign) {
		this.reign = reign;
	}

}