public class Animal {

	private String name;
	private String family;

	public String getName() {
		return this.name;
	}

	/**
	 * 
	 * @param name
	 */
	public void setName(String name) {
		this.name = name;
	}

	public String getFamily() {
		return this.family;
	}

	/**
	 * 
	 * @param family
	 */
	public void setFamily(String family) {
		this.family = family;
	}

}